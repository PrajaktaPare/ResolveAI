import { useState, useEffect, useCallback, useRef } from "react";
import logger from "@/utils/logger";

/**
 * Lightweight data-fetching hook with loading/error state and refetch.
 * @param {() => Promise<any>} fetcher - async function returning data
 * @param {Array} deps - dependency array that triggers a refetch
 */
export function useFetch(fetcher, deps = []) {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const fetcherRef = useRef(fetcher);
  fetcherRef.current = fetcher;

  const load = useCallback(async () => {
    setLoading(true);
    setError(null);
    try {
      const result = await fetcherRef.current();
      setData(result);
    } catch (err) {
      logger.error("useFetch error", err);
      setError(err);
    } finally {
      setLoading(false);
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, deps);

  useEffect(() => {
    load();
  }, [load]);

  return { data, loading, error, refetch: load };
}
